for (c in list) {
    pr <- pca.hic(c, npc=1, asGRangesList=TRUE)
    filename <- paste(odir, "/", c@xgi@seqinfo@seqnames, ".txt", sep="")
    write.table(pr, filename, quote=F, col.names=F, row.names=F, sep="\t")
}
